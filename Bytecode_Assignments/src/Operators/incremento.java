package Operators;

public class incremento {

	public static void main(String[] args) {
	int a=10,b=0,c=0;
	b=a++; // post increment
	System.out.println("B value is: "+b);
	System.out.println("A value is: "+a);
	c=++a; // pre  increment
	System.out.println("C value is: "+c);
	System.out.println("A value is: "+a);
	

	}

}
